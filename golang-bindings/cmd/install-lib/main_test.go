func TestIsHTTPStatus(t *testing.T) {
	err := &httpStatusError{StatusCode: http.StatusNotFound}
	if !isHTTPStatus(err, http.StatusNotFound) {
		t.Fatal("expected status match")
	}
	if isHTTPStatus(err, http.StatusForbidden) {
		t.Fatal("expected status mismatch")
	}
	if isHTTPStatus(errors.New("boom"), http.StatusNotFound) {
		t.Fatal("expected non-http error mismatch")
	}
}

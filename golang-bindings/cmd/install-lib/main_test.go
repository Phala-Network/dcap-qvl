func TestFetchLatestReleaseTag(t *testing.T) {
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if r.URL.Path != "/repos/org/repo/releases/latest" {
			http.NotFound(w, r)
			return
		}
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write([]byte(`{"tag_name":"v9.9.9"}`))
	}))
	defer srv.Close()

	tag, err := fetchLatestReleaseTag(context.Background(), srv.Client(), srv.URL, "org/repo")
	if err != nil {
		t.Fatalf("fetchLatestReleaseTag error: %v", err)
	}
	if tag != "v9.9.9" {
		t.Fatalf("got tag %q", tag)
	}
}

func TestFetchLatestReleaseTagHTTPErrorType(t *testing.T) {
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		http.NotFound(w, r)
	}))
	defer srv.Close()

	_, err := fetchLatestReleaseTag(context.Background(), srv.Client(), srv.URL, "org/repo")
	if err == nil {
		t.Fatal("expected error")
	}
	var httpErr *httpStatusError
	if !errors.As(err, &httpErr) {
		t.Fatalf("expected httpStatusError, got %T", err)
	}
	if httpErr.StatusCode != http.StatusNotFound {
		t.Fatalf("unexpected status code: %d", httpErr.StatusCode)
	}
}

func TestFileMatchesSHA256(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "x.a")
	if err := os.WriteFile(path, []byte("abc"), 0o644); err != nil {
		t.Fatalf("write file: %v", err)
	}
	const shaABC = "ba7816bf8f01cfea414140de5dae2223b00361a396177a9cb410ff61f20015ad"

	ok, err := fileMatchesSHA256(path, shaABC)
	if err != nil {
		t.Fatalf("fileMatchesSHA256 error: %v", err)
	}
	if !ok {
		t.Fatal("expected checksum match")
	}
}


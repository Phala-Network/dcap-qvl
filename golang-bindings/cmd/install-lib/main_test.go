func TestPlatformAssetName(t *testing.T) {
	tests := []struct {
		goos    string
		goarch  string
		want    string
		wantErr bool
	}{
		{"linux", "amd64", "libdcap_qvl_linux_amd64.a", false},
		{"linux", "arm64", "libdcap_qvl_linux_arm64.a", false},
		{"darwin", "amd64", "libdcap_qvl_darwin_amd64.a", false},
		{"darwin", "arm64", "libdcap_qvl_darwin_arm64.a", false},
		{"windows", "amd64", "", true},
		{"linux", "386", "", true},
	}

	for _, tt := range tests {
		got, err := platformAssetName(tt.goos, tt.goarch)
		if tt.wantErr && err == nil {
			t.Fatalf("%s/%s: expected error", tt.goos, tt.goarch)
		}
		if !tt.wantErr && err != nil {
			t.Fatalf("%s/%s: unexpected error: %v", tt.goos, tt.goarch, err)
		}
		if got != tt.want {
			t.Fatalf("%s/%s: got %q want %q", tt.goos, tt.goarch, got, tt.want)
		}
	}
}

func TestParseChecksums(t *testing.T) {
	input := `
# generated by sha256sum
1111111111111111111111111111111111111111111111111111111111111111  libdcap_qvl_linux_amd64.a
2222222222222222222222222222222222222222222222222222222222222222 *libdcap_qvl_darwin_arm64.a
`
	got, err := parseChecksums(input)
	if err != nil {
		t.Fatalf("parseChecksums error: %v", err)
	}
	if got["libdcap_qvl_linux_amd64.a"] != strings.Repeat("1", 64) {
		t.Fatalf("missing/invalid linux checksum")
	}
	if got["libdcap_qvl_darwin_arm64.a"] != strings.Repeat("2", 64) {
		t.Fatalf("missing/invalid darwin checksum")
	}
}

func TestParseChecksumsRejectsBadLine(t *testing.T) {
	_, err := parseChecksums("abcdef")
	if err == nil {
		t.Fatal("expected parse error")
	}
}

